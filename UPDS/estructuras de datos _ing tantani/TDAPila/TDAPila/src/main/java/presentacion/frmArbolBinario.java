    class PanelArbol extends JPanel {
        private static final int RADIO_NODO = 25;
        private static final int ESPACIO_HORIZONTAL = 50;
        private static final int ESPACIO_VERTICAL = 70;

        @Override
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2d = (Graphics2D) g;
            g2d.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

            if (!arbol.estaVacio()) {
                int ancho = getWidth();
                dibujarNodo(g2d, arbol.getRaiz(), ancho / 2, 40, ancho / 4);
            } else {
                g2d.setColor(Color.GRAY);
                g2d.setFont(new Font("Arial", Font.ITALIC, 18));
                String mensaje = "Árbol vacío - Inserte datos";
                FontMetrics fm = g2d.getFontMetrics();
                int x = (getWidth() - fm.stringWidth(mensaje)) / 2;
                int y = getHeight() / 2;
                g2d.drawString(mensaje, x, y);
            }
        }

        private void dibujarNodo(Graphics2D g2d, nodoAB nodo, int x, int y, int separacion) {
            if (nodo == null) return;

            // Dibujar líneas a los hijos primero (para que queden detrás)
            if (nodo.getHijoI() != null) {
                int xIzq = x - separacion;
                int yIzq = y + ESPACIO_VERTICAL;
                g2d.setColor(new Color(100, 100, 100));
                g2d.setStroke(new BasicStroke(2));
                g2d.drawLine(x, y, xIzq, yIzq);
                dibujarNodo(g2d, nodo.getHijoI(), xIzq, yIzq, separacion / 2);
            }

            if (nodo.getHijoD() != null) {
                int xDer = x + separacion;
                int yDer = y + ESPACIO_VERTICAL;
                g2d.setColor(new Color(100, 100, 100));
                g2d.setStroke(new BasicStroke(2));
                g2d.drawLine(x, y, xDer, yDer);
                dibujarNodo(g2d, nodo.getHijoD(), xDer, yDer, separacion / 2);
            }

            // Dibujar el nodo actual
            g2d.setColor(new Color(63, 81, 181)); // Azul
            g2d.fillOval(x - RADIO_NODO, y - RADIO_NODO, RADIO_NODO * 2, RADIO_NODO * 2);

            g2d.setColor(Color.WHITE);
            g2d.setStroke(new BasicStroke(3));
            g2d.drawOval(x - RADIO_NODO, y - RADIO_NODO, RADIO_NODO * 2, RADIO_NODO * 2);

            // Dibujar el valor del nodo
            g2d.setColor(Color.WHITE);
            g2d.setFont(new Font("Arial", Font.BOLD, 16));
            String valor = nodo.getDato().toString();
            FontMetrics fm = g2d.getFontMetrics();
            int textX = x - fm.stringWidth(valor) / 2;
            int textY = y + fm.getAscent() / 2 - 2;
            g2d.drawString(valor, textX, textY);
        }
    }

