    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try {
            String texto = txtDato.getText().trim();
            if (texto.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingrese un dato", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int dato = Integer.parseInt(texto);
            arbol.insertarAB(dato);
            txtDato.setText("");
            txtDato.requestFocus();
            panelArbol.repaint();
            txtResultado.setText("Dato " + dato + " insertado correctamente");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un número válido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            String texto = txtDato.getText().trim();
            if (texto.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingrese un dato a eliminar", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int dato = Integer.parseInt(texto);
            boolean eliminado = arbol.eliminarAB(dato);

            if (eliminado) {
                txtDato.setText("");
                txtDato.requestFocus();
                panelArbol.repaint();
                txtResultado.setText("Dato " + dato + " eliminado correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "El dato no existe en el árbol", "Información", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un número válido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

